import React from "react";
import "./Product.css";
import { useStateValue } from "./StateProvider";

function Product({ id, title, image, price, rating }) {
  const [{ basket }, dispatch] = useStateValue();

  const addToBasket = () => {
    // push into that basket array
    // the way it does it:
    // it doesnt push into the array, it dispatches an action with some information
    // and then we listen to that action with information

    dispatch({
      // inside reducer, it checks the ACTION.TYPE
      type: "ADD_TO_BASKET",
      item: {
        id: id,
        title: title,
        image: image,
        price: price,
        rating: rating,
      },
    });
  };

  return (
    <div className="product">
      <div className="product__info">
        <p className="product__title">{title}</p>
        <p className="product__price">
          <small>$</small>
          <strong>{price}</strong>
        </p>
        <div className="product__rating">
          {Array(rating) // creates an array of rating.length => 5
            .fill() // fill it with empty values
            // map it through and print stars
            .map((_) => (
              <p>‚≠ê</p>
            ))}
        </div>
      </div>

      <img src={image} alt="" />
      <button onClick={addToBasket}>Add to basket</button>
    </div>
  );
}

export default Product;
